void sinus_wave_generator(double duration, double sample_rate, double amplitude, double frequency, int16_t sinus_value[], size_t size){
    /* Generation of sinusoid

    Args:
        duration (double): Duration of signal (in seconds)
        sample_rate (scalar): Sampling rate
        amplitude (float): Amplitude of sinusoid
        frequency (float): Frequency of sinusoid
        phase (float): Phase of sinusoid (Default value = 0)
        sinus_value (array) : Array to store the discrete sinus signal.
        size (size_t) : Size of the array.
    */

	int sample_N = (int)sample_rate*duration; // Samples per period.

	for(int i = 0; i < sample_N ; i++){
		double timestep =  (double)i / sample_rate;
		double temp = (amplitude*sin(2.0 * M_PI * frequency * timestep));
		sinus_value[i] = (int16_t)(temp);
	}
}

